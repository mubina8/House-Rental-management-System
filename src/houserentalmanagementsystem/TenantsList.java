    public ArrayList<Tenant> userList(){
        ArrayList<Tenant> userList = new ArrayList<>();
               try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            connection = DriverManager.getConnection(
                    "jdbc:sqlserver://localhost:1433;databaseName=LandLordDatabase;selectMethod=cursor", "sa", "123456");
            String query = "SELECT * FROM Tenants inner join registration ON registration.email=Tenants.Email";
            Statement state = connection.createStatement();
            ResultSet resultSet = state.executeQuery(query);
            Tenant tenant;
            while(resultSet.next())
            {
                tenant = new Tenant(resultSet.getString("ApartmentNo"),resultSet.getString("Email"),resultSet.getString("TEmail"), resultSet.getString("TenantName"), resultSet.getString("TOccupation"), resultSet.getString("TphoneNo"),resultSet.getString("TenantType"));
                userList.add(tenant);
            }
            
               }catch (Exception e) {
            e.printStackTrace();
        }
               return userList;
    }
    
    public void show_user(){
        ArrayList<Tenant> list = userList();
        DefaultTableModel model = (DefaultTableModel)TenantsTable.getModel();
        Object[] row = new Object[7];
        for(int i=0; i<list.size(); i++)
        {
            row[0]=list.get(i).getApartmentNo();
            row[1]=list.get(i).getEmail();
            row[2]=list.get(i).getTEmail();
            row[3]=list.get(i).getTenantName();
            row[4]=list.get(i).getTOccupation();
            row[5]=list.get(i).getTphoneNo();
            row[6]=list.get(i).getTenantType();
            model.addRow(row);
        }
    }

