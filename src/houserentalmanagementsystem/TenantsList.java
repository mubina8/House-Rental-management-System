    private void searchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchKeyReleased
         String searchRes = search.getText();
          DefaultTableModel model = (DefaultTableModel)TenantsTable.getModel();
           int i=TenantsTable.getRowCount();
            while(i-->0)
            {
               model.removeRow(i);
            }
         try{
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            connection = DriverManager.getConnection(
                    "jdbc:sqlserver://localhost:1433;databaseName=LandLordDatabase;selectMethod=cursor", "sa", "123456");
            String query = "SELECT * from Tenants where TEmail LIKE'%"+searchRes+"%' OR TphoneNo LIKE'%"+searchRes+"%'";
            PreparedStatement pstmt=connection.prepareStatement(query);
            ResultSet res = pstmt.executeQuery();
         while(res.next())
            {
               java.util.Vector v = new java.util.Vector();
              
               v.add(res.getString("ApartmentNo"));
               v.add(res.getString("Email"));
               v.add(res.getString("TEmail"));
               v.add(res.getString("TenantName"));
               v.add(res.getString("TOccupation"));
               v.add(res.getString("TphoneNo"));
               v.add(res.getString("TenantType"));
               model.addRow(v);
            }
            
         }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);  
         }
    }//GEN-LAST:event_searchKeyReleased

